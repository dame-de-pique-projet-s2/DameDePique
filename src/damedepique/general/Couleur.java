package damedepique.general;

/**
 * <p>
 *   Cette énumération regroupe les quatre couleurs (ou enseignes) d'un jeu de 
 *   52 cartes français. Parmi les cartes à jouer occidentales, il s'agit du 
 *   système d'enseignes le plus répandu.
 * </p>
 * <p>
 *   Les quatre couleurs sont les suivantes : 
 *   le carreau (♦), le coeur (♥), le pique (♠) et le trèfle (♣).
 * </p>
 * 
 * @author Julien B.
 * @version 1.0
 */
public enum Couleur {

	Carreau,
	Coeur,
	Pique,
	Trefle
	
}
